    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:

        int catRow = jTable1.getSelectedRow();
        int brandRow = jTable2.getSelectedRow();
        String name = jTextField3.getText();

        if (jButton4.getText().equals("Add Product")) {
            if (catRow == -1) {
                JOptionPane.showMessageDialog(this, "Please select a category", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (brandRow == -1) {
                JOptionPane.showMessageDialog(this, "Please select a brand", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (name.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter product title", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {
                try {

                    ResultSet rs = MySQL.search("SELECT * FROM `product` WHERE `title`=?", name);
                    if (rs.next()) {
                        JOptionPane.showMessageDialog(this, "Product already exists", "Warning", JOptionPane.WARNING_MESSAGE);
                    } else {
                        String brandId = jTable2.getValueAt(brandRow, 0).toString();
                        String catId = jTable1.getValueAt(catRow, 0).toString();
                        MySQL.iud("INSERT INTO `product` (`title`,`brand_id`,`category_id`) VALUES (?,?,?)", name, brandId, catId);
                        LOG.info("INFO:: add new product named:"+name);
                        loadProductsTable();
                        JOptionPane.showMessageDialog(this, "Product added success", "Success", JOptionPane.INFORMATION_MESSAGE);
                        jTextField3.setText("");
                        home.loadProductsTableGRN();
                    }

                } catch (Exception e) {
                    e.printStackTrace();
                    LOG.info("INFO:: add new product error:"+e);
                }
            }
        } else {
            if (name.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter product title", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {
                int proRow = jTable3.getSelectedRow();
                if (proRow == -1) {
                    JOptionPane.showMessageDialog(this, "Please select a product", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    String proId = jTable3.getValueAt(proRow, 0).toString();
                    String uquery = "UPDATE `product` SET `title`=? ";
                    List<Object> params = new ArrayList<>();
                    params.add(name);
                    if (catRow == -1 && brandRow == -1) {

                    } else if (catRow == -1 && brandRow != -1) {
                        int option = JOptionPane.showConfirmDialog(this, "Do you want to update product category too?", "Confimation", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
                        if (option == JOptionPane.YES_OPTION) {
                            String catId = jTable1.getValueAt(catRow, 0).toString();
                            uquery += " ,`category_id`=?";
                            params.add(catId);
                        }else{
                            return;
                        }

                    } else if (catRow != -1 && brandRow == -1) {
                        int option = JOptionPane.showConfirmDialog(this, "Do you want to update product brand too?", "Confimation", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
                        if (option == JOptionPane.YES_OPTION) {
                            String brandId = jTable2.getValueAt(brandRow, 0).toString();
                            uquery += " ,`brand_id`=?";
                            params.add(brandId);
                        }else{
                            return;
                        }

                    } else {
                        int option = JOptionPane.showConfirmDialog(this, "Do you want to update product category and brand too?", "Confimation", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
                        if (option == JOptionPane.YES_OPTION) {
                            String catId = jTable1.getValueAt(catRow, 0).toString();
                            String brandId = jTable2.getValueAt(brandRow, 0).toString();
                            uquery += " ,`category_id`=? ,`brand_id`=?";
                            params.add(catId);
                            params.add(brandId);
                        }else{
                            return;
                        }

                    }

                    uquery += " WHERE `id`=?";
                    params.add(proId);

                    MySQL.iud(uquery, params.toArray());
                    LOG.info("INFO:: update selected product pid:"+proId);
                    loadProductsTable();
                    JOptionPane.showMessageDialog(this, "Product added success", "Success", JOptionPane.INFORMATION_MESSAGE);
                    jTextField3.setText("");
                    jButton4.setText("Add Product");
                    home.loadProductsTableGRN();
                }
            }
        }


    }//GEN-LAST:event_jButton4ActionPerformed

