    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText();
        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter category name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                if (jButton2.getText().equals("Add Category")) {
                    ResultSet rs = MySQL.search("SELECT * FROM `category` WHERE `name`=?", name);
                    if (rs.next()) {
                        JOptionPane.showMessageDialog(this, "Category already exists", "Warning", JOptionPane.WARNING_MESSAGE);
                    } else {
                        MySQL.iud("INSERT INTO `category` (`name`) VALUES (?)", name);
                        loadCategoriesTable();
                        LOG.info("INFO:: add new categoty named:"+name);
                        JOptionPane.showMessageDialog(this, "Category Added", "Success", JOptionPane.INFORMATION_MESSAGE);
                        jTextField1.setText("");
                        home.loadCategoryComboGRN();
                    }
                } else {
                    int r = jTable1.getSelectedRow();
                    if (r == -1) {
                        JOptionPane.showMessageDialog(this, "Please select category", "Warning", JOptionPane.WARNING_MESSAGE);
                    } else {
                        String catId = jTable1.getValueAt(r, 0).toString();
                        ResultSet rs = MySQL.search("SELECT * FROM `category` WHERE `name`=? AND `id`!=?", name, catId);
                        if (rs.next()) {
                            JOptionPane.showMessageDialog(this, "Category already exists", "Warning", JOptionPane.WARNING_MESSAGE);
                        } else {
                            MySQL.iud("UPDATE `category` SET `name` = ? WHERE `id`=?", name, catId);
                            loadCategoriesTable();
                            LOG.info("INFO:: update category name :"+name);
                            JOptionPane.showMessageDialog(this, "Category updated", "Success", JOptionPane.INFORMATION_MESSAGE);
                            jTextField1.setText("");
                            jButton2.setText("Add Category");
                            home.loadCategoryComboGRN();
                        }

                    }
                }

            } catch (Exception e) {
                e.printStackTrace();
                LOG.error("ERROR:: add new product class, add new categery error:"+e);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

