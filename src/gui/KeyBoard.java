    private void jButton42ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton42ActionPerformed
        // TODO add your handling code here:
        this.textField.setText(jTextField1.getText());
        if (getParent() instanceof Home) {
            Home homeParent = (Home) getParent(); // Cast to Home
            if (this.textField == homeParent.jTextField10) { // Check if it's the search field
                homeParent.searchUserUM(); // Call the search method in Home
            } else if (this.textField == homeParent.jTextField14) {
                homeParent.loadSupplierTableGRN(jTextField1.getText());
            }else if (this.textField == homeParent.jTextField18) {
                homeParent.searchProductsTableGRN();
            }
        } else if (getParent() instanceof AddNewSupplier) {
            AddNewSupplier suppliersParent = (AddNewSupplier) getParent();
            if (this.textField == suppliersParent.jTextField1) { // For search text field in Suppliers
                suppliersParent.loadCompaniesTableByName(jTextField1.getText()); // Call the supplier search method
            } else if (this.textField == suppliersParent.jTextField4) {
                suppliersParent.loadBranchesTableByName(jTextField1.getText());
            }
        }

        this.dispose();
    }//GEN-LAST:event_jButton42ActionPerformed

